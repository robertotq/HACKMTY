            String tipo = "";
            Object datos[] = new Object[4];
            while(resultSet.next())
            {
                tipo = resultSet.getString("Tipo");
                System.out.println(tipo);
                switch (tipo){
                
                    case "Hotel":
                    TotHotel++;    
                        break;
                    case "Atraccion":
                    TotAtra++;
                        break;
                        
                    case "Restaurante":
                    TotResta++;
                        break;
                
                    default:
                        System.out.println("Se escribiÃ³ mal y no se reconoce el tipo");
                        break;
                }
            } 
            
            String Query2 = "SELECT Nombre FROM Ciudades WHERE CodigoM = " + i;
        Statement st2 = Conection.createStatement();
            java.sql.ResultSet resultSet2;
            resultSet2 = st2.executeQuery(Query2);
            
            
            while(resultSet2.next())
                datos[0] = resultSet2.getString("Nombre");
            
            datos[1] = TotHotel;
            datos[2] = TotAtra;
            datos[3] = TotResta;
            
            
            
            modeloTabla.addRow(datos);
            
            TotHotel = 0;
            TotAtra = 0;
            TotResta = 0;
            
